let image1 = "1_p";
function changeImg1() {
    let image= document.getElementById("img1");
    if (image1 === '1_p') {

        image1 = '1_c';

    } else if (image1 === '1_c') {

        image1 = '1_m';

    } else if (image1 === '1_m') {

        image1 = '1_p';

    }
    image.src =  image1 + '.jpg';
    
}
